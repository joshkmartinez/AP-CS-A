import java.util.Scanner;
import java.lang.Math;
class Lesson_7_Activity_Two {
public static void main(String[] args) {
Scanner scan = new Scanner (System.in);
System.out.println("Enter a positive three digit integar.");
int number = scan.nextInt();
int b = number % 100;
int digThree = b % 10;
int c = number - b;
int d = c / 10;
int digOne = d / 10;
int digTwo = b - digThree;
digTwo = digTwo / 10;
int sum = digOne + digTwo + digThree;
System.out.println("Here are the digits:\n" + digOne + "\n" + digTwo + "\n" + digThree);
System.out.print("\n" + digOne + " + " + digTwo + " + " + digThree +" = " + sum);
} }